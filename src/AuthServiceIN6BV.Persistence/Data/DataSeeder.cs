using AuthServiceIN6BV.Domain.Entities;
using AuthServiceIN6BV.Application.Services;
using AuthServiceIN6BV.Domain.Constants;
using Microsoft.EntityFrameworkCore;
namespace AuthServiceIN6BV.Persistence.Data;
 public class DataSeeder
{
    public static async Task SeedAsync(ApplicationDbContext context)
    {
        if (!context.Roles.Any())
        {
            var roles = new List<Role>
            {
                new()
                {
                    Id = UuidGenerator.GenerateRoleId(),
                    Name = RoleConstants.ADMIN_ROLE
                },
                new()
                {
                    Id = UuidGenerator.GenerateRoleId(),
                    Name = RoleConstants.USER_ROLE
                }
            };
            await context.Roles.AddRangeAsync(roles);
            await context.SaveChangesAsync();
        }
        if (!await context.Users.AnyAsync())
        {
            var adminRole = await context.Roles.FirstOrDefaultAsync(r => r.Name == RoleConstants.ADMIN_ROLE);
            if (adminRole != null)
            {
                var password = new PasswordHashService();
                var profileId = UuidGenerator.GenerateUserId();
                var emailId = UuidGenerator.GenerateUserId();
                var UsersRoleId = UuidGenerator.GenerateRoleId();
                var userId = UuidGenerator.GenerateUserId();
                var adminUser = new User
                {
                    Id = userId,
                    Name = "Admin",
                    Surname = "User",
                    Username = "admin",
                    Email = "admin@ksports.local",
                    Password = password.HashPassword("Admin1234!"),
                    Status = true,
                    UserProfile = new userProfile
                    {
                        Id = profileId,
                        UserId = userId,
                        ProfilePicture = string.Empty,
                        Phone = string.Empty
                    },
                    UserEmail = new UserEmail
                    {
                        Id = emailId,
                        UserId = userId,
                        EmailVerified = true,
                        EmailVerificationToken = null,
                        EmailVerificationTokeExpiry = null
                    },
                    UserRoles = [
                        new UserRole{
                            Id = UsersRoleId,   
                            UserId = userId,
                            RoleId = adminRole.Id
                        }
                    ]

                };
                await context.Users.AddAsync(adminUser);
                await context.SaveChangesAsync();
            }
        }

    }   
}